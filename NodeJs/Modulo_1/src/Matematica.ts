export const sum = (num1: number, num2: number): number => {
  return num1 + num2;
};

export const sub = (num1: number, num2: number): number => {
  return num1 - num2;
};

export const div = (num1: number, num2: number): number => {
  return num1 / num2;
};

export const mult = (num1: number, num2: number): number => {
  return num1 * num2;
};

